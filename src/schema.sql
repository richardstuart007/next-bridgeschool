CREATE TABLE IF NOT EXISTS public.tdb_database
(
    db_dbid integer NOT NULL GENERATED BY DEFAULT AS IDENTITY ( INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 2147483647 CACHE 1 ),
    db_name character varying(16) COLLATE pg_catalog."default" NOT NULL,
    CONSTRAINT tdb_database_pkey PRIMARY KEY (db_name),
    CONSTRAINT tdb_database_db_dbid_key UNIQUE (db_dbid)
);

CREATE TABLE IF NOT EXISTS public.ths_history
(
    hs_hsid integer NOT NULL GENERATED BY DEFAULT AS IDENTITY ( INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 2147483647 CACHE 1 ),
    hs_datetime timestamp with time zone NOT NULL,
    hs_sbid integer,
    hs_owner character varying(16) COLLATE pg_catalog."default" NOT NULL,
    hs_subject character varying(32) COLLATE pg_catalog."default",
    hs_qqid integer[],
    hs_questions integer,
    hs_ans integer[],
    hs_points integer[],
    hs_maxpoints integer,
    hs_totalpoints integer,
    hs_correctpercent integer,
    hs_usid integer,
    hs_rfid integer DEFAULT 0,
    CONSTRAINT ths_history_pkey PRIMARY KEY (hs_hsid)
);

CREATE TABLE IF NOT EXISTS public.tlg_logging
(
    lg_lgid integer NOT NULL GENERATED BY DEFAULT AS IDENTITY ( INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 2147483647 CACHE 1 ),
    lg_severity character(1) COLLATE pg_catalog."default",
    lg_caller character varying COLLATE pg_catalog."default" DEFAULT ''::character varying,
    lg_functionname character varying COLLATE pg_catalog."default" NOT NULL,
    lg_msg character varying COLLATE pg_catalog."default" NOT NULL,
    lg_datetime timestamp with time zone NOT NULL,
    CONSTRAINT tlg_logging_pkey PRIMARY KEY (lg_lgid)
);

CREATE TABLE IF NOT EXISTS public.tow_owner
(
    ow_owid integer NOT NULL GENERATED BY DEFAULT AS IDENTITY ( INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 2147483647 CACHE 1 ),
