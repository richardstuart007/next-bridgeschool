CREATE TABLE IF NOT EXISTS public.tqq_questions
(
    qq_qqid integer NOT NULL GENERATED BY DEFAULT AS IDENTITY ( INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 2147483647 CACHE 1 ),
    qq_owner character varying(16) COLLATE pg_catalog."default" NOT NULL,
    qq_subject character varying(32) COLLATE pg_catalog."default" NOT NULL,
    qq_sbid integer NOT NULL,
    qq_seq smallint NOT NULL,
    qq_detail character varying(256) COLLATE pg_catalog."default",
    qq_points integer[],
    qq_ans text[] COLLATE pg_catalog."default",
    qq_rounds text[] COLLATE pg_catalog."default",
    qq_north text[] COLLATE pg_catalog."default",
    qq_east text[] COLLATE pg_catalog."default",
    qq_south text[] COLLATE pg_catalog."default",
    qq_west text[] COLLATE pg_catalog."default",
    qq_rfid integer DEFAULT 0,
    qq_help text COLLATE pg_catalog."default",
    CONSTRAINT tqq_questions_pkey PRIMARY KEY (qq_owner, qq_subject, qq_seq),
    CONSTRAINT tqq_questions_qq_qqid_key UNIQUE (qq_qqid)
);

CREATE TABLE IF NOT EXISTS public.trf_reference
(
